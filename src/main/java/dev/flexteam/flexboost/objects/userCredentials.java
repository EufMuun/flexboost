package dev.flexteam.flexboost.objects;

public class userCredentials {
    String email;
    String password;

    void setEmail(String email){
        this.email = email;
    }

    void setPassword(){
        this.password = password;
    }

    String getEmail(){
        return this.email;
    }

    String getPassword(){
        return this.password;
    }

}
